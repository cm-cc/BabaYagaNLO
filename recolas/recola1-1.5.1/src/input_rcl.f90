                           'with arguments'
      write(nx,*) '          m = ',m
      write(nx,*) '          g = ',g
      write(nx,*) '         has no effects.'
      write(nx,*)
      call toomanywarnings(201)
    endif
  endif

   mass_z = m
  width_z = g

  end subroutine set_pole_mass_z_rcl

