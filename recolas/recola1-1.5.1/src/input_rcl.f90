  subroutine set_onshell_mass_z_rcl (m,g)

  ! This subroutine sets the on-shell mass and width of the Z boson
  ! to "m" and "g" respectively. This is done by setting the pole mass
  ! "mass_z" to "m/sqrt(1+g^2/m^2)" and the pole width "width_z" to
  ! "g/sqrt(1+g^2/m^2)".

  real(dp), intent(in) :: m,g

  if (m.le.0d0.or.g.lt.0d0) then
    if (warnings(202).le.warning_limit) then
      warnings(202) = warnings(202) + 1
      call openOutput
      write(nx,*)
      write(nx,*) &
        'ERROR 202: set_onshell_mass_z_rcl called with wrong arguments'
      write(nx,*)
      call toomanywarnings(202)
    endif
    call istop (ifail,1)
  endif

  if (processes_generated) then
    if (warnings(203).le.warning_limit) then
      warnings(203) = warnings(203) + 1
      call openOutput
      write(nx,*)
      write(nx,*) 'WARNING 203: Processes already generated.'
      write(nx,*) '             The call of set_onshell_mass_z_rcl ', &
                           'with arguments'
      write(nx,*) '          m = ',m
      write(nx,*) '          g = ',g
      write(nx,*) '         has no effects.'
      write(nx,*)
      call toomanywarnings(203)
    endif
  endif

   mass_z = m / sqrt ( 1d0 + g**2/m**2 )
  width_z = g / sqrt ( 1d0 + g**2/m**2 )

  end subroutine set_onshell_mass_z_rcl

