  ! argument "x" (if it already exists, its content is cleared).
  ! By a subsequent call of set_output_file_rcl with a different
  ! argument "y", the file "x" remains open (until the call
  ! reset_recola_rcl), but the output is now printed on "y".
  ! If the set_output_file_rcl is again called with argument
  ! "x", further output is then appended to the open file "x".
  ! By the call of reset_recola_rcl all opened output files are
  ! closed and, by further call of set_output_file_rcl with argument
  ! "x", the content of the existing file "x" is cleared.

  character(*), intent(in) :: x

