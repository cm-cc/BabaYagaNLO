    if (warnings(298).le.warning_limit) then
      warnings(298) = warnings(298) + 1
      write(*,*)
      write(*,*) ' WARNING 298:'
      write(*,*) ' the name of output-file'
      write(*,*)
      write(*,'(a)') trim(x)
      write(*,*)
      write(*,*) ' is too long and has been truncated to'
      write(*,*)
      write(*,'(a999)') trim(x)
      write(*,*)
      call toomanywarnings(298)
    endif
  endif

  outputfile = trim(x)

  if (recolascreen.eq.1) then
    write(*,*)
    write(*,'(1x,75("x"))')
    write(*,*)
    if (trim(outputfile).eq.'*') then
      write(*,*) 'From now on the output of RECOLA is written ', &
                 'on the screen.'
    else
      write(*,*) 'From now on the output of RECOLA is written ', &
                 'to the file ',trim(outputfile)
    endif
