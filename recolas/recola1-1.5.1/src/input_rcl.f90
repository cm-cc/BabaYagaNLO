  implicit none

!---------------------------------------------------------------------
! Physical variables
!---------------------------------------------------------------------

! Pole Masses:
! The Z,W masses and width are computed from the on-shell masses
! and width:
! mass_z_onshell =  91.1876d0, width_z_onshell = 2.4952d0,
! mass_w_onshell =  80.385d0,  width_w_onshell = 2.085d0,
  real(dp) :: mass_z  =  91.153480619182744d0, width_z  = 2.4942663787728243d0
  real(dp) :: mass_w  =  80.357973609877547d0, width_w  = 2.0842989982782196d0
  real(dp) :: mass_h  = 125.d0,                width_h  = 0.d0
  real(dp) :: mass_el =   0.d0
  real(dp) :: mass_mu =   0.d0,                width_mu = 0.d0
  real(dp) :: mass_ta =   0.d0,                width_ta = 0.d0
  real(dp) :: mass_u  =   0.d0
  real(dp) :: mass_d  =   0.d0
  real(dp) :: mass_c  =   0.d0,                width_c  = 0.d0
  real(dp) :: mass_s  =   0.d0
  real(dp) :: mass_t  = 173.2d0,               width_t  = 0.d0
  real(dp) :: mass_b  =   0.d0,                width_b  = 0.d0

! Regularization of collinear singularities:
! Dimensional regularization is used for collinear singularities of
! massless fermions.
! Massive fermions are treated according to the variables "light_x"
! (x = el,mu,ta,u,d,c,s,t,b):
! - light_x = .true.
!   Fermion "x" is light (terms proportional to the mass are
!   discarded). Mass regularization is used for collinear divergences
!   with mass regulator given by the pole mass of fermion "x".
! - light_x = .false.
!   Fermion "x" is heavy (full mass dependence is kept).
! If the mass of fermion "x" is zero, "light_x" is irrelevant.
  logical :: light_el = .true.
  logical :: light_mu = .true.
  logical :: light_ta = .true.
  logical :: light_u  = .true.
  logical :: light_d  = .true.
  logical :: light_c  = .true.
  logical :: light_s  = .true.
  logical :: light_t  = .false.
  logical :: light_b  = .true.

! Regularization of soft singularities:
! "reg_soft" defines the regularization of soft singularities
! generated by massless photons and gluons:
! - reg_soft = 1:
!   dimensional regularization is used for soft singularities
!   ("lambda" irrelevant).
! - reg_soft = 2:
!   mass regularization is used for soft singularities with mass
!   regulator "lambda".
! REMARK: mass regularization for soft singularities (reg_soft = 2)
!         is allowed only in combination with mass regularization
!         for collinear singularities of light fermions. Therefore
!         if reg_soft = 2, no massless fermions are allowed
!         (and the code stops).
  integer :: reg_soft = 1;   real(dp) :: lambda = 100d0

! UV and IR dimensional regulators:
! "DeltaUV"  is the coefficient of the UV pole in the MSbar scheme
!            1/ep - EulerConstant + ln(4*pi)
! "muUV"     is the UV scale
! "DeltaIR"  is the coefficient of the IR single pole
! "DeltaIR2" is the coefficient of the IR double pole
! "muIR"     is the IR scale
  real(dp) :: DeltaUV = 0d0,                 muUV = 100d0
  real(dp) :: DeltaIR = 0d0, DeltaIR2 = 0d0, muIR = 100d0

! alpha_s, renormalization scale and flavour scheme:
! "Qren" and "als" are the initialization values for the
! renormalization scale and for alpha_s.
! "Nfren" is the number of active flavours for alpha_s renormalization.
! "als" should correspond to the physical value of alpha_s at the
! scale "Qren" computed in the "Nfren"-flavour scheme.
! Accepted values for "Nfren" are -1,3,4,5,6
! Nfren = -1      -> all quarks with mass less than "Qren" are active
!                    flavours
! Nfren = 3,4,5,6 -> the 3,4,5,6 lightest quarks are the active
!                    flavours
  real(dp) :: als   = 0.118d0
  real(dp) :: Qren  = 91.1876d0
  integer  :: Nfren = 5

! Schemes for renormalization of alpha:
! - ew_reno_scheme = 1 -> gfermi scheme:
!   alpha is determined from G_F ("gf").
!   Universal corrections enhanced by large mass ratios are resummed.
!   ("al0", "alZ" are irrelevant)
! - ew_reno_scheme = 2 -> alpha0 scheme:
!   alpha is renormalized at p^2 = 0 ("al0").
!   No resummation of universal corrections
!   ("gf", "alZ" are irrelevant)
! - ew_reno_scheme = 3 -> alphaZ scheme:
!   alpha is renormalized at p^2 = mz^2 ("alZ").
!   No resummation of universal corrections
!   ("gf", "al0" are irrelevant)
! - ew_reno_scheme = 4 -> alphaMSbar scheme:
!   alpha is renormalized with MSbar counterterms.
!   "alMS" should correspond to the physical value of alpha at the
!   scale "Qren_alMS" computed with the actual values of quark masses.
! The variable ew_reno_scheme determines the renomaliztion scheme for
! all process where a fixed scheme is adopted.
! To use a mixed renomalization scheme for a selected process, the
! user must use the specific routines in process_definition_rcl.f90.
  integer  :: ew_reno_scheme = 1
  real(dp) :: gf        = 1.16637d-5
  real(dp) :: al0       = 1d0/137.035999679d0
  real(dp) :: alZ       = 1d0/128.936d0
  real(dp) :: alMS      = 1d0/127.930d0
  real(dp) :: Qren_alMS = 91.1876d0

  ! Treatment of W and Z masses in the computation of alpha_gf in
  ! the gfermi scheme:
  ! masses_in_gf_reno_scheme = 0:
  !  alpha_GF = G_F*sqrt(2)/pi*Re(MW^2)*( 1 - Re(MW^2)/Re(MZ^2) )
  ! masses_in_gf_reno_scheme = 1:
  !  alpha_GF = G_F*sqrt(2)/pi*abs( MW^2*( 1 - MW^2/MZ^2) )
  integer  :: masses_in_gf_reno_scheme = 1

! Complex mass scheme:
! - complex_mass_scheme = 1:
!   The complex mass scheme of hep-ph/0505042 is used.
! - complex_mass_scheme = 0:
!   The on-shell scheme is used, i.e. just the real part of the
!   self-energies are taken for the computation of the counterterms.
!   The imaginary part of the masses are kept just in the
!   propagators (also in fermion propagator i_/(pslash-m), m is kept
!   complex).
  integer :: complex_mass_scheme = 1

! Self-energies of resonant particles
  logical :: resSE = .true.
  logical :: transverse_resonance = .true.

! Dynamic settings
  integer :: dynamic_settings = 0

! Switch for the correction of external momenta to restore the
! mass-shell conditions and momentum conservation
  logical :: momenta_correction = .true.

!---------------------------------------------------------------------
! Latex
!---------------------------------------------------------------------

! Generate a .tex file to draw the branches of the processes:
! 0 -> the .tex file is not generated.
! 1 -> the .tex file is generated without colour structures.
! 2 -> the .tex file is generated with colour structures.
  integer :: draw = 0

!---------------------------------------------------------------------
! Print options
!---------------------------------------------------------------------

! Print amplitudes
! writeMat = 0 -> nothing is printed.
! writeMat = 1 -> The non vanishining amplitudes A(c,h) are printed
!                 at LO and NLO (if NLO is computed) for each coulour
!                 structure "c" and helicity "h"
! writeMat = 2 -> In addition to writeMat=1, the NLO (if computed)
!                 contributions to A(c,h) from bare-loop, counterterms
!                 and rational part are also printed.
  integer :: writeMat  = 0

! Print summed squared amplitude
! writeMat2 = 0 -> nothing is printed.
! writeMat2 = 1 -> The non vanishining squared amplitudes A2
!                  summed/averaged over colour and helicities are
!                  printed at LO and NLO (if NLO is computed).
! writeMat2 = 2 -> In addition to writeMat2=1, the NLO (if computed)
!                  contributions to A2 from bare-loop, counterterms
!                  and rational part are also printed.
! writeMat2 = 3 -> In addition to writeMat2=1, also the squared
!                  amplitudes A2h summed/averaged over colour are
!                  printed at LO and NLO (if NLO is computed) for
!                  each helicity "h".
  integer :: writeMat2 = 0

! Print matrix elements squared with colour correlation (if computed)
! writeCor = 0 -> nothing is printed.
! writeCor = 1 -> The non vanishining colour-correlated Born squared
!                 amplitudes A2c(n,m) are printed for each emitter
!                 "n" and spectator "m".
  integer :: writeCor = 0

! Print RAM needed in the present run of Recola:
! writeRAM = 0 -> nothing is printed.
! writeRAM = 1 -> RAM permanently used by process generation and process
!                 computation is printed.
! writeRAM = 2 -> In addition to writeRAM=1, RAM temporally used by
!                 process generation is printed.
  integer :: writeRAM = 0

