  subroutine get_alpha_rcl (a)

  ! This subroutine extracts the actual value of the fixed-scheme
  ! alpha, setting "a" to the value of alpha of the fixed
  ! renormalization scheme.

  real(dp), intent(out) :: a

  real(dp)    :: rew2,rez2
  complex(dp) :: mw2,mz2

  if (ew_reno_scheme.eq.1) then
    if (.not.processes_generated) then
      if (algf.ge.0d0) then
        alpha = algf
      elseif (masses_in_gf_reno_scheme.eq.0) then
        rew2 = mass_w**2
        rez2 = mass_z**2
        alpha = gf*sq2/pi*rew2*( 1d0 - rew2/rez2 )
      elseif (masses_in_gf_reno_scheme.eq.1) then
        mw2 = mass_w**2 - cId0*width_w*mass_w
        mz2 = mass_z**2 - cId0*width_z*mass_z
        alpha = gf*sq2/pi*abs( mw2*( 1d0 - mw2/mz2 ) )
      endif
    endif
    a = alpha
  elseif (ew_reno_scheme.eq.2) then
    a = al0
  elseif (ew_reno_scheme.eq.3) then
    a = alZ
  elseif (ew_reno_scheme.eq.4) then
    a = alMS
  endif

  end subroutine get_alpha_rcl

