  subroutine set_alphas_masses_rcl (mc,mb,mt,gc,gb,gt)

  ! This subroutine allows to set the values of the squared quark
  ! masses for the running of alpha_s; their values are not used in
  ! the rest of the computation.
  ! The squared masses of the 3 heaviest quarks are set to
  ! "mc^2*sqrt(1+gc^2/mc^2)", "mb^2*sqrt(1+gb^2/mb^2)" and
  ! "mt^2*sqrt(1+gt^2/mt^2)" respectively.
  ! "mc", "mb", "mt" must satisfy 0 <= mc <= mb <= mt.
  ! The optional arguments "gc", "gb", "gt"  are ignored if the
  ! on-shell renormalization scheme is used.

  real(dp), intent(in)           :: mc,mb,mt
  real(dp), intent(in), optional :: gc,gb,gt

  if (processes_generated) then
    if (warnings(263).le.warning_limit) then
      warnings(263) = warnings(263) + 1
      call openOutput
      write(nx,*)
      write(nx,*) 'WARNING 263: Processes already generated.'
      write(nx,*) '             The call of set_alphas_masses_rcl ', &
                           'with arguments'
      write(nx,*) '              mc = ',mc
      write(nx,*) '              mb = ',mb
      write(nx,*) '              mt = ',mt
      if (present(gc)) &
      write(nx,*) '              gc = ',gc
      if (present(gb)) &
      write(nx,*) '              gb = ',gb
      if (present(gt)) &
      write(nx,*) '              gt = ',gt
      write(nx,*) '             has no effects.'
      write(nx,*)
      call toomanywarnings(263)
    endif
  endif

  if ((mc.lt.0d0) .or. (mb .lt. mc) .or. (mt .lt. mb)) then
    if (warnings(264).le.warning_limit) then
      warnings(264) = warnings(264) + 1
      call openOutput
      write(nx,*)
      write(nx,*) &
        'ERROR 264: set_alphas_masses_rcl called with wrong arguments.'
      write(nx,*) &
        '           Masses need to be positive and to satisfy 0 <= mc <= mb <= mt.'
      write(nx,*)
      call toomanywarnings(264)
    endif
    call istop (ifail,1)
  endif

  if (complex_mass_scheme .eq. 0 .and. &
      (present(gc) .or. present(gb) .or. present(gt))) then
    if (warnings(265).le.warning_limit) then
      warnings(265) = warnings(265) + 1
      call openOutput
      write(nx,*)
      write(nx,*) &
        'WARNING 265: Widths in set_alphas_masses_rcl are ignored'
      write(nx,*) &
        '             when complex_mass_scheme is 0.'
      write(nx,*)
      call toomanywarnings(265)
    endif
  end if

  mq(1:3) = 0d0

  if (present(gc)) then
    if (gc.lt.0d0) then
      if (warnings(266).le.warning_limit) then
        warnings(266) = warnings(266) + 1
        call openOutput
        write(nx,*)
        write(nx,*) &
          'ERROR 266: set_alphas_masses_rcl called with wrong arguments.'
        write(nx,*) &
          '           Widths can not be negative.'
        write(nx,*)
        call toomanywarnings(266)
      endif
      call istop (ifail,1)
    endif
    mq(4) = sqrt(abs(mc**2 - cId0*mc*gc))
  else
    mq(4) = mc
  endif

  if (present(gb)) then
    if (gb.lt.0d0) then
      if (warnings(267).le.warning_limit) then
        warnings(267) = warnings(267) + 1
        call openOutput
        write(nx,*)
        write(nx,*) &
          'ERROR 267: set_alphas_masses_rcl called with wrong arguments.'
        write(nx,*) &
          '           Widths can not be negative.'
        write(nx,*)
        call toomanywarnings(267)
      endif
      call istop (ifail,1)
    endif
    mq(5) = sqrt(abs(mb**2 - cId0*mb*gb))
  else
    mq(5)   = mb
  endif

  if (present(gt)) then
    if (gt.lt.0d0) then
      if (warnings(268).le.warning_limit) then
        warnings(268) = warnings(268) + 1
        call openOutput
        write(nx,*)
        write(nx,*) &
          'ERROR 268: set_alphas_masses_rcl called with wrong arguments.'
        write(nx,*) &
          '           Widths can not be negative.'
        write(nx,*)
        call toomanywarnings(268)
      endif
      call istop (ifail,1)
    endif
    mq(6) = sqrt(abs(mt**2 - cId0*mt*gt))
  else
    mq(6)   = mt
  endif

  use_active_qmasses = .true.

  end subroutine set_alphas_masses_rcl

