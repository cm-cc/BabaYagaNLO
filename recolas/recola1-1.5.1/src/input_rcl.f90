  subroutine set_alphas_rcl (a,Q,Nf)

  ! This subroutine sets the values of alpha_s ("als") to "a", the
  ! renormalization scale "Qren" to "Q" and the number of active
  ! quark flavours "Nfren" to "Nf".
  ! The choosen value of "a" should correspond to the physical value
  ! of alpha_s at the scale "Q" computed in the "Nf"-flavour scheme.
  ! Accepted values for "Nf" are -1,3,4,5,6
  ! Nf = -1      -> all quarks with mass less than "Q" are active
  !                 flavours
  ! Nf = 3,4,5,6 -> the 3,4,5,6 lightest quarks are the active
  !                 flavours

  integer,  intent(in) :: Nf
  real(dp), intent(in) :: a,Q

  integer :: i

  if (a.le.0d0.or.Q.le.0d0) then
    if (warnings(259).le.warning_limit) then
      warnings(259) = warnings(259) + 1
      call openOutput
      write(nx,*)
      write(nx,*) &
        'ERROR 259: set_alphas_rcl called ', &
               'with wrong arguments'
      write(nx,*)
      call toomanywarnings(259)
    endif
    call istop (ifail,1)
  endif

  select case (Nf)
  case (-1,3,4,5,6)
  case default
    if (warnings(260).le.warning_limit) then
      warnings(260) = warnings(260) + 1
      call openOutput
      write(nx,*)
      write(nx,*) 'ERROR 260: set_alphas_rcl called ', &
                          'with a wrong number of '
      write(nx,*) '           light flavours Nf (accepted values are Nf = -1,3,4,5,6)'
      write(nx,*)
      call toomanywarnings(260)
    endif
    call istop (ifail,1)
  end select

  als   = a
  Qren  = Q
  Nfren = Nf

  if (processes_generated) then
    ! set Nlq, the number of active flavours for alpha_s renormalization.
    select case (Nfren)
    case (-1)
      ! Nlq is the number of quarks lighter than Qren
      Nlq = 0
      do i = 1,6
        if (mq2(i).lt.Qren**2) Nlq = Nlq + 1
      enddo
    case (3,4,5)
      if (mq2(Nfren+1).eq.0d0) then
        if (warnings(261).le.warning_limit) then
          warnings(261) = warnings(261) + 1
          call openOutput
          write(nx,*)
          write(nx,*) 'ERROR 261: Wrong number Nfren of light flavours ', &
                             'for alphas renormaliztion'
          write(nx,*) '           (Nfren can not be smaller than the ', &
                             'number of massless quarks)'
          write(nx,*)
          call toomanywarnings(261)
        endif
        call istop (ifail,1)
      endif
      Nlq = Nfren
    case (6)
      Nlq = Nfren
    case default
      if (warnings(262).le.warning_limit) then
        warnings(262) = warnings(262) + 1
        call openOutput
        write(nx,*)
        write(nx,*) 'ERROR 262: Wrong value for the number Nfren of light ', &
                           'flavours for alphas renormaliztion'
        write(nx,*) '           (accepted values are Nfren = -1,3,4,5,6)'
        write(nx,*)
        call toomanywarnings(262)
      endif
      call istop (ifail,1)
    end select
  endif

  end subroutine set_alphas_rcl

